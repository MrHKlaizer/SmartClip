// ║                                                                              ║
// ║  Этот файл содержит реальный код методов, объявленных в Watchdog.h.        ║
// ║  Логика простая: поток запускается, каждые 5 секунд проверяет              ║
// ║  ClipboardManager, и если тот «завис» — перезапускает его.                 ║
// ╚══════════════════════════════════════════════════════════════════════════════╝

#include "Watchdog.h"          // Описание класса Watchdog
#include "ClipboardManager.h"  // Нужен чтобы вызывать методы isRunning() и start()
#include <QDebug>              // qDebug() — вывод отладочных сообщений в консоль Qt Creator

// ─── Конструктор ─────────────────────────────────────────────────────────────
// Вызывается при создании объекта: Watchdog watchdog(&clipboard)
// Синтаксис ": QThread(parent)" — передаём родителя в базовый класс QThread.
// ", m_manager(manager)" — сохраняем указатель на ClipboardManager.
// ", m_running(false)"   — изначально поток не запущен.
Watchdog::Watchdog(ClipboardManager *manager, QObject *parent)
    : QThread(parent)      // Инициализируем базовый класс QThread
    , m_manager(manager)   // Запоминаем кого охранять
    , m_running(false)     // Флаг «работаю» = выключен до вызова start()
{
    // Тело конструктора пустое — всё уже сделано в списке инициализации выше
}

// ─── Остановка потока ─────────────────────────────────────────────────────────
// Вызывается из главного потока когда программа закрывается.
void Watchdog::stop()
{
    m_running = false;  // Сбрасываем флаг — при следующей проверке поток выйдет из цикла
    wait();             // Ждём пока фоновый поток полностью завершится
                        // (чтобы не было «гонки» при удалении объектов)
}

// ─── Главный цикл потока ─────────────────────────────────────────────────────
// Этот метод автоматически вызывается Qt когда мы делаем watchdog.start().
// Он выполняется В ОТДЕЛЬНОМ ПОТОКЕ — параллельно с интерфейсом программы.
void Watchdog::run()
{
    m_running = true;  // Устанавливаем флаг что мы работаем
    qDebug() << "Watchdog запущен";  // Сообщение в консоль разработчика

    // Бесконечный цикл: крутится пока m_running = true
    while (m_running) {

        // Засыпаем на 5 секунд — не нагружаем процессор попусту.
        // За это время пользователь может свободно работать с интерфейсом.
        QThread::sleep(5);

        // Проверяем снова флаг после сна — вдруг за это время вызвали stop()
        if (!m_running) break;

        // Главная проверка: жив ли ClipboardManager?
        // isRunning() возвращает false если мониторинг буфера завис или остановился.
        if (!m_manager->isRunning()) {
            qDebug() << "Watchdog: ClipboardManager не отвечает, перезапускаем...";
            m_manager->start();  // Перезапускаем мониторинг — SmartClip снова начнёт
                                 // записывать историю копирования
        }
    }

    qDebug() << "Watchdog остановлен";  // Поток завершил работу
}
