#pragma once  // Защита от двойного подключения файла (стандартная практика в C++)

// ─── Версия приложения ────────────────────────────────────────────────────────
// #define означает «текстовую замену» — везде где встречается APP_VERSION
// компилятор подставит строку "1.0.2"
//
// Формат версии: MAJOR.MINOR.PATCH
//   MAJOR — кардинальные изменения (новый интерфейс, новая архитектура)
//   MINOR — новые функции (маштаб, плашка обновления)
//   PATCH — исправления багов без новых функций
//
// При публикации новой версии меняй это значение и загружай новый релиз на GitHub.
// Программа установленная у пользователей сравнит свою версию с этой на GitHub
// и предложит обновиться, если на GitHub версия новее.
#define APP_VERSION "1.0.2"

// ─── GitHub репозиторий ───────────────────────────────────────────────────────
// Адрес репозитория на GitHub в формате "имя_пользователя/название_репозитория".
// Используется для формирования ссылки на GitHub API — сервис, который возвращает
// информацию о последнем релизе (версию и ссылку на скачивание).
#define GITHUB_REPO    "MrHKlaizer/SmartClip"

// Полный URL для запроса к GitHub API.
// Пример результата: "https://api.github.com/repos/MrHKlaizer/SmartClip/releases/latest"
// Когда SmartClip запускается, он делает запрос по этому адресу и получает JSON
// с информацией о последней версии — номер версии, описание и ссылку на установщик.
#define GITHUB_API_URL "https://api.github.com/repos/" GITHUB_REPO "/releases/latest"
