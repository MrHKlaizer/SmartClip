// ║  DCompBlur.h — Стеклянный эффект размытия фона (Windows)                  ║
// ║                                                                              ║
// ║  Отвечает за красивый «матовый» фон под колонками SmartClip.               ║
// ║  Использует технологию DirectComposition — встроенный в Windows механизм   ║
// ║  для создания эффектов прозрачности и размытия без нагрузки на процессор.  ║
// ║                                                                              ║
// ║  Как это работает визуально:                                                ║
// ║  ┌────────────────────────────────────────────┐                             ║
// ║  │░░░░░░░░│          прозрачно       │░░░░░░░│  ← размытие только под      ║
// ║  │░ левая ░│                          │░правая░│    колонками, центр чистый  ║
// ║  │░колонка░│                          │░колонка░│                            ║
// ║  └────────────────────────────────────────────┘                             ║
// ╚══════════════════════════════════════════════════════════════════════════════╝

#pragma once
#include <windows.h>  // Заголовочный файл Windows API — дaёт доступ к типу HWND и другим системным типам

// ─── Структура с координатами для размытия ────────────────────────────────────
// Содержит все числовые параметры, которые описывают ГДЕ именно рисовать размытие.
// Передаётся в функции ниже чтобы они знали размеры и положение колонок.
struct BlurRects {
    int screenW, screenH;  // Ширина и высота всего экрана в пикселях
    int leftX,   topY;     // X и Y координата верхнего-левого угла левой колонки
    int leftW;             // Ширина левой колонки
    int rightX,  rightW;   // X координата и ширина правой колонки
    int colH;              // Высота обеих колонок (они одинаковой высоты)
};

// ─── Функции управления размытием ─────────────────────────────────────────────
// Эти функции объявлены здесь, а реализованы в DCompBlur.cpp.
// Такое разделение — стандарт C++: .h описывает ЧТО есть, .cpp описывает КАК работает.

// Инициализация: создаёт невидимые служебные окна для размытия.
// Вызывается один раз при запуске. Возвращает true если всё прошло успешно.
// mainHwnd — «дескриптор» (идентификатор) главного окна SmartClip в Windows.
bool  dcompBlurInit    (HWND mainHwnd, const BlurRects &r);

// Показать размытие: рисует матовые области под колонками.
// Вызывается каждый раз когда SmartClip открывается или меняет размер.
void  dcompBlurShow    (HWND mainHwnd, const BlurRects &r);

// Скрыть размытие: убирает матовые области когда SmartClip сворачивается.
void  dcompBlurHide    ();

// Завершение работы: освобождает память и системные ресурсы.
// Вызывается при закрытии программы.
void  dcompBlurShutdown();
